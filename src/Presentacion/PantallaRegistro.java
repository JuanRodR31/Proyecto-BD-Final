    public void registrarUsuario (){
        FuncionesDAO registro = new FuncionesDAO();
        FuncionesDAO admin = new FuncionesDAO();
        String campoP = comboBoxPais.getSelectedItem().toString();
        System.out.println(campoP);
        int idPais = admin.convertirPaisAID(campoP);
        System.out.println(idPais);
        String campoN = campoNombres.getText();
        String campoA = campoApellidos.getText();
        String campoU = campoUsername.getText();
        String campoC = campoContrasena.getText();
        String contrasenaEncriptada = registro.encriptarConSHA256(campoC);
        boolean userOAdmin = botonUsuario.isSelected();
        String tipoUsuario;
        if (!userOAdmin){
            tipoUsuario="admin";
        }
        else {
            tipoUsuario="user";
        }
        if (campoN.isEmpty() || campoA.isEmpty() || campoU.isEmpty() || campoC.isEmpty()){
            textoError.setText("No pueden existir campos vacios");
        }
        else{
           Usuario usuario= new Usuario (campoN, campoA, campoU, contrasenaEncriptada, tipoUsuario, idPais);
           boolean reg=registro.registrarUsuario (usuario);
           if (reg){
               textoError.setText("usuario "+ campoU +" agregado.");
           }
           else {
               textoError.setText("no se pudo agregar el usuario");
           }
        } 
        
        if (tipoUsuario=="admin"){
            PantallaLogin pLogin = new PantallaLogin();
            pLogin.setVisible(true);
            dispose();
        }
        else {
            PantallaSuscribirse pSuscribirse = new PantallaSuscribirse();
            pSuscribirse.setVisible(true);
            dispose();
        }
    }
