    private void botonAgregarAlbumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarAlbumActionPerformed
        FuncionesDAO admin = new FuncionesDAO();
        
        int codigoAlbum= admin.obtenerProximoIdAlbum();
        String titulo = textFieldTituloAlbum.getText();
        String fecha = textFieldFechaLanzamiento.getText();
        
        java.util.Date utilFecha = null;

        try {
            utilFecha = new SimpleDateFormat("dd-MM-yyyy").parse(fecha);
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

        java.sql.Date sqlFecha = null;
        if (utilFecha != null) {
            sqlFecha = new java.sql.Date(utilFecha.getTime());
        }
        String empresaDiscografica= (String) comboBoxEmpresaDiscografica.getSelectedItem();
        int idEmpresaDiscografica= 1;
        String albumOEP = rbuttonAlbum.isSelected() ? "album" : "ep";
        
        Album album= new Album(codigoAlbum, titulo, sqlFecha, idEmpresaDiscografica, albumOEP);
        boolean agregoAlbum =admin.agregarAlbumOEp(album);
        if (agregoAlbum){
            textoResultadoAgregarAlbum.setText("Se Agrego el album correctamente");
        }
        else{
            textoResultadoAgregarAlbum.setText("Ocurrio un error al agregar el album");
        }
        actualizarComboBoxAlbum();
    }//GEN-LAST:event_botonAgregarAlbumActionPerformed

